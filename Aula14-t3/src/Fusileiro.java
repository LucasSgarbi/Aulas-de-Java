public class Fusileiro extends Bot {
	private int tiros;
	private int armas;
	private int mortes;
	public int getTiros() {
		return tiros;
	}
	public void setTiros(int tiros) {
		this.tiros = tiros;
	}
	public int getArmas() {
		return armas;
	}
	public void setArmas(int armas) {
		this.armas = armas;
	}
	public int getMortes() {
		return mortes;
	}
	public void setMortes(int mortes) {
		this.mortes = mortes;
	}
	
	

}
