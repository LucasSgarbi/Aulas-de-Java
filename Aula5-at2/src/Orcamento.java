public class Orcamento extends Documento{
	public int itens;
	public double custo;
	public int prazo;
	
	public int getItens() {
		return itens;
	}
	public double getCusto() {
		return custo;
	}
	public int getPrazo() {
		return prazo;
	}
	
	public void setItens(int itens) {
		this.itens = itens;
	}
	public void setCusto(double custo) {
		this.custo = custo;
	}
	public void setPrazo(int prazo) {
		this.prazo = prazo;
	}
	
	
	
}
