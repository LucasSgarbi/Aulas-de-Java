
public class Veiculo {
	private String placa;
	private int  ano;
	private double valor;
	private Acessorio acesorrio;
	
	public void setPlaca(String placa) {
		this.placa = placa;
	}
	public void setAno(int ano) {
		this.ano = ano;
	}
	public void setValor(double valor) {
		this.valor = valor;
	}
	public void setAcesorrio(Acessorio acesorrio) {
		this.acesorrio = acesorrio;
	}
	public String getPlaca() {
		return placa;
	}
	public int getAno() {
		return ano;
	}
	public double getValor() {
		return valor;
	}
	public Acessorio getAcesorrio() {
		return acesorrio;
	}
	
	
	
}
