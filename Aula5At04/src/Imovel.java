public class Imovel {
	private long id;
	private String descricao;
	private int area;
	
	public void setId(long id) {
		this.id = id;
	}
	public void setDescricao(String descricao) {
		this.descricao = descricao;
	}
	public void setArea(int area) {
		this.area = area;
	}
	
	public long getId() {
		return id;
	}
	public String getDescricao() {
		return descricao;
	}
	public int getArea() {
		return area;
	}
	
	
}
