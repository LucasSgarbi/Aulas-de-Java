public class Locacao extends Contrato {
	private double valor;
	private int prazo;
	
	public void setValor(double valor) {
		this.valor = valor;
	}
	public void setPrazo(int prazo) {
		this.prazo = prazo;
	}
	
	public double getValor() {
		return valor;
	}
	public int getPrazo() {
		return prazo;
	}
	
	
	
}
