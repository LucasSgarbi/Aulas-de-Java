public class Pessoa {
	private String cpf;
	private String nome;
	private Email email;
	
	public void setCpf(String cpf) {
		this.cpf = cpf;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public void setEmail(Email email) {
		this.email = email;
	}
	
	public String getCpf() {
		return cpf;
	}
	public String getNome() {
		return nome;
	}
	public Email getEmail() {
		return email;
	}
	
	
	
}
