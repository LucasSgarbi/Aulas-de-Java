public class Diciplina {
	private String nome;
	private Diciplina diciplina;
	
	public void setNome(String nome) {
		this.nome = nome;
	}
	public void setDiciplina(Diciplina diciplina) {
		this.diciplina = diciplina;
	}
	
	public String getNome() {
		return nome;
	}
	public Diciplina getDiciplina() {
		return diciplina;
	}
	

}
