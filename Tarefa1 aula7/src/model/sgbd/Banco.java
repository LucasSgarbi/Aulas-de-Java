package model.sgbd;

public class Banco {
	private String nome;
	private String conexao;
	
	public void setNome(String nome) {
		this.nome = nome;
	}
	public void setConexao(String conexao) {
		this.conexao = conexao;
	}
	
	public String getNome() {
		return nome;
	}
	public String getConexao() {
		return conexao;
	}
	
	
	
	

}
